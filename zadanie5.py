import math
print(7//2) #dzielenie bez reszty - wynikiem jest liczba całkowita(niezaokrąglona)
print(7/2)  #dzielenie z resztą
print(round(3.25))   #round(x) zwraca najbliższą, względem x liczbę całkowitą
print(round(3.64))   #round(x) zwraca najbliższą, względem x liczbę całkowitą
print(math.floor(3.5))  #floor(x) zwraca największą liczbę całkowitą mniejszą lub równą x